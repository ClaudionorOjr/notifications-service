//* Importando o tipo Notification vindo do prisma
import { Notification as RawNotification } from '@prisma/client';
import { Notification } from '@application/entities/notification';
import { Content } from '@application/entities/content';

export class PrismaNotificationMapper {
  //* Convertando da camada de aplicação para a camada de banco de dados
  //? Com o static não precisa instanciar essa classe
  static toPrisma(notification: Notification) {
    return {
      id: notification.id,
      content: notification.content.value,
      category: notification.category,
      recipientId: notification.recipientId,
      readAt: notification.readAt,
      cancledAt: notification.canceledAt,
      createdAt: notification.createdAt,
    };
  }

  //* Convertando da camada de banco de dados para a camada aplicação
  static toDomain(raw: RawNotification): Notification {
    return new Notification(
      {
        category: raw.category,
        content: new Content(raw.content),
        recipientId: raw.recipientId,
        readAt: raw.readAt,
        canceledAt: raw.canceledAt,
        createdAt: raw.createdAt,
      },
      raw.id,
    );
  }
}
